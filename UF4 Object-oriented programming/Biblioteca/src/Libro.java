public class Libro{
    String titulo;
    public String autor;
    public String tema;
    public int numPaginas;
    public int ISBN;
    public String catalogo;

    public Libro (String titulo , String autor , String tema , int numPaginas , int ISBN){
        this.titulo = titulo;
        this.autor = autor;
        this.tema = tema;
        this.numPaginas = numPaginas;
        this.ISBN = ISBN;
    }
}

